/**
 * Complex Pattern Generators for Lit Web Components
 *
 * Dedicated generators for components that need behavioral code beyond
 * the simple property-to-template mapping handled by the base generator:
 * - textarea: character count display + debounced SR announcements
 * - radio-group: child sync, roving tabindex, keyboard navigation
 * - checkbox-group: child sync, multi-value FormData assembly
 * - segmented-control: child sync, position data, keyboard navigation
 * - date-picker: calendar dialog, keyboard grid nav, date math
 * - file-upload: drag-drop, file validation, file list management
 */

import type { ComponentSchema, PropDef } from '@civui/schema/types';
import { toPascalCase, toComponentName } from '../utils/naming.js';

// ---------------------------------------------------------------------------
// Shared helpers
// ---------------------------------------------------------------------------

function litPropType(prop: PropDef): string {
  switch (prop.type) {
    case 'string': case 'enum': return 'String';
    case 'boolean': return 'Boolean';
    case 'number': return 'Number';
    case 'array': return 'Array';
    default: return 'String';
  }
}

function tsType(prop: PropDef, name: string, componentName: string): string {
  if (prop.type === 'enum' && prop.values) return `${componentName}${toComponentName(`-${name}`)}`;
  if (prop.type === 'array' && prop.items) return `${componentName}Option[]`;
  switch (prop.type) {
    case 'string': return 'string';
    case 'boolean': return 'boolean';
    case 'number': return 'number';
    case 'array': return 'unknown[]';
    default: return 'string';
  }
}

function defaultValue(prop: PropDef): string {
  if (prop.default === undefined) return '';
  if (Array.isArray(prop.default)) return ' = []';
  if (typeof prop.default === 'string') return ` = '${prop.default}'`;
  if (typeof prop.default === 'boolean') return ` = ${prop.default}`;
  if (typeof prop.default === 'number') return ` = ${prop.default}`;
  return '';
}

function generatePropDeclarations(schema: ComponentSchema): string {
  const componentName = toComponentName(schema.name);
  return Object.entries(schema.props)
    .map(([name, prop]) => {
      const type = tsType(prop, name, componentName);
      const litType = litPropType(prop);
      const def = defaultValue(prop);
      const optional = prop.default === undefined ? '?' : '';
      const reflectOpt = prop.reflect ? ', reflect: true' : '';
      const attrOpt = prop.attribute ? `, attribute: '${prop.attribute}'` : '';
      return `  @property({ type: ${litType}${reflectOpt}${attrOpt} }) ${name}${optional}: ${type}${def};`;
    })
    .join('\n');
}

function generateEnumTypes(schema: ComponentSchema): string {
  const lines: string[] = [];
  const componentName = toComponentName(schema.name);
  for (const [name, prop] of Object.entries(schema.props)) {
    if (prop.type === 'enum' && prop.values) {
      const typeName = `${componentName}${toComponentName(`-${name}`)}`;
      const union = prop.values.map((v) => `'${v}'`).join(' | ');
      lines.push(`export type ${typeName} = ${union};`);
    }
  }
  return lines.length > 0 ? lines.join('\n') + '\n' : '';
}

function header(schema: ComponentSchema): string {
  return `// Auto-generated by @civui/codegen — do not edit\n// Source: packages/schema/src/components/${schema.name}.schema.ts\n`;
}

function tagNameMap(schema: ComponentSchema): string {
  const className = toPascalCase(schema.name);
  return [
    '',
    'declare global {',
    '  interface HTMLElementTagNameMap {',
    `    '${schema.name}': ${className};`,
    '  }',
    '}',
    '',
  ].join('\n');
}

function jsdoc(schema: ComponentSchema): string {
  const componentName = toComponentName(schema.name);
  return [
    '/**',
    ` * CivUI ${componentName}`,
    ' *',
    ` * ${schema.description}`,
    ' *',
    ` * @element ${schema.name}`,
    ' */',
  ].join('\n');
}

