// Checkbox Group Generator
// ---------------------------------------------------------------------------

export function generateCheckboxGroupLit(schema: ComponentSchema): string {
  const className = toPascalCase(schema.name);
  const propDecls = generatePropDeclarations(schema);
  const enumTypes = generateEnumTypes(schema);

  return `${header(schema)}
import { html, nothing } from 'lit';
import { customElement, property } from 'lit/decorators.js';
import { CivFormElement, dispatch, renderLegend, renderHint, renderError } from '@civui/core';
import type { CivCheckbox } from './civ-checkbox.js';

${enumTypes}${jsdoc(schema)}
@customElement('${schema.name}')
export class ${className} extends CivFormElement {
${propDecls}

  protected override _defaultValue = '';
  private _boundOnChildChange = this._onChildChange.bind(this);
  private _boundStopChildInput = this._stopChildInput.bind(this);

  override connectedCallback(): void {
    super.connectedCallback();
    this.addEventListener('civ-change', this._boundOnChildChange as EventListener);
    this.addEventListener('civ-input', this._boundStopChildInput as EventListener);
  }

  override disconnectedCallback(): void {
    super.disconnectedCallback();
    this.removeEventListener('civ-change', this._boundOnChildChange as EventListener);
    this.removeEventListener('civ-input', this._boundStopChildInput as EventListener);
  }

  protected override willUpdate(changed: Map<string, unknown>): void {
    super.willUpdate(changed);
    if (changed.has('legend')) {
      this.label = this.legend;
    }
  }

  override firstUpdated(): void {
    this._syncCheckboxNames();
    this._syncCheckboxDisabled();
    this._syncCheckboxTile();

    if (this.value) {
      this._syncCheckboxChecked();
    } else {
      this._readCheckedFromChildren();
    }

    this._defaultValue = this.value;
  }

  override updated(changed: Map<string, unknown>): void {
    super.updated(changed);
    if (changed.has('name')) this._syncCheckboxNames();
    if (changed.has('disabled')) this._syncCheckboxDisabled();
    if (changed.has('tile')) this._syncCheckboxTile();
    if (changed.has('value') && changed.get('value') !== undefined) this._syncCheckboxChecked();
  }

  protected override _syncFormValue(): void {
    this._updateGroupFormValue();
  }

  override formDisabledCallback(disabled: boolean): void {
    super.formDisabledCallback(disabled);
    this._syncCheckboxDisabled();
  }

  override render() {
    const layoutClass =
      this.orientation === 'horizontal'
        ? 'civ-group-layout--horizontal'
        : 'civ-group-layout--vertical';

    return html\`
      <fieldset
        class="civ-fieldset"
        aria-describedby="\${this._ariaDescribedBy || nothing}"
        aria-invalid="\${this.error ? 'true' : nothing}"
        aria-required="\${this.required || nothing}"
        ?disabled="\${this.disabled}"
      >
        \${renderLegend({ legend: this.legend, required: this.required })}
        \${renderHint(this._hintId, this.hint, true)}
        \${renderError(this._errorId, this.error, true)}
        <div class="\${layoutClass}"></div>
      </fieldset>
    \`;
  }

  getCheckedValues(): string[] {
    return this._parseValue(this.value);
  }

  private _getCheckboxes(): CivCheckbox[] {
    return Array.from(this.querySelectorAll('civ-checkbox')) as CivCheckbox[];
  }

  private _parseValue(val: string): string[] {
    return val
      ? val.split(',').map((v) => v.trim()).filter(Boolean)
      : [];
  }

  private _serializeValue(values: string[]): string {
    return values.join(',');
  }

  private _syncCheckboxNames(): void {
    this._getCheckboxes().forEach((cb) => {
      if (this.name) cb.name = this.name;
      cb.disableAnalytics = true;
    });
  }

  private _groupDisabledSet = new WeakSet<Element>();

  private _syncCheckboxDisabled(): void {
    const children = this._getCheckboxes();
    if (this.disabled) {
      children.forEach((cb) => {
        if (!cb.disabled) this._groupDisabledSet.add(cb);
        cb.disabled = true;
      });
    } else {
      children.forEach((cb) => {
        if (this._groupDisabledSet.has(cb)) {
          cb.disabled = false;
        }
      });
      this._groupDisabledSet = new WeakSet();
    }
  }

  private _syncCheckboxTile(): void {
    this._getCheckboxes().forEach((cb) => {
      cb.tile = this.tile;
    });
  }

  private _syncCheckboxChecked(): void {
    const selected = this._parseValue(this.value);
    this._getCheckboxes().forEach((cb) => {
      cb.checked = selected.includes(cb.value);
    });
  }

  private _readCheckedFromChildren(): void {
    const values: string[] = [];
    this._getCheckboxes().forEach((cb) => {
      if (cb.checked) values.push(cb.value);
    });
    this.value = this._serializeValue(values);
    this._updateGroupFormValue();
  }

  private _updateGroupFormValue(): void {
    const values = this._parseValue(this.value);
    if (values.length === 0) {
      this.updateFormValue(null);
      return;
    }
    const fd = new FormData();
    for (const v of values) {
      fd.append(this.name, v);
    }
    this.updateFormValue(fd);
  }

  private _stopChildInput(e: Event): void {
    if (e.target !== this) e.stopPropagation();
  }

  private _onChildChange(e: Event): void {
    if (e.target === this) return;
    e.stopPropagation();

    const values: string[] = [];
    this._getCheckboxes().forEach((cb) => {
      if (cb.checked) values.push(cb.value);
    });

    this.value = this._serializeValue(values);
    this._updateGroupFormValue();

    dispatch(this, 'civ-input', { values: this.getCheckedValues() });
    dispatch(this, 'civ-change', { values: this.getCheckedValues() });
    this.sendAnalytics('change');
  }

  override formResetCallback(): void {
    this.value = this._defaultValue;
    this.error = '';
    this._syncCheckboxChecked();
    this._updateGroupFormValue();
    dispatch(this, 'civ-reset');
  }
}
${tagNameMap(schema)}`;
}

// ---------------------------------------------------------------------------
// Segmented Control Generator
// ---------------------------------------------------------------------------

export function generateSegmentedLit(schema: ComponentSchema): string {
  const className = toPascalCase(schema.name);
  const propDecls = generatePropDeclarations(schema);
  const enumTypes = generateEnumTypes(schema);

  return `${header(schema)}
import { html, nothing } from 'lit';
import { customElement, property } from 'lit/decorators.js';
import { CivFormElement, dispatch, renderLegend, renderHint, renderError, resolveGroupNavIndex, isRtl } from '@civui/core';
import type { CivSegment } from './civ-segment.js';

${enumTypes}${jsdoc(schema)}
@customElement('${schema.name}')
export class ${className} extends CivFormElement {
${propDecls}

  protected override _defaultValue = '';
  private _boundOnChildChange = this._onChildChange.bind(this);
  private _boundStopChildInput = this._stopChildInput.bind(this);
  private _boundOnKeydown = this._onKeydown.bind(this);

  override connectedCallback(): void {
    super.connectedCallback();
    this.addEventListener('civ-change', this._boundOnChildChange as EventListener);
    this.addEventListener('civ-input', this._boundStopChildInput as EventListener);
    this.addEventListener('keydown', this._boundOnKeydown as EventListener);
  }

  override disconnectedCallback(): void {
    super.disconnectedCallback();
    this.removeEventListener('civ-change', this._boundOnChildChange as EventListener);
    this.removeEventListener('civ-input', this._boundStopChildInput as EventListener);
    this.removeEventListener('keydown', this._boundOnKeydown as EventListener);
  }

  override willUpdate(changed: Map<string, unknown>): void {
    super.willUpdate(changed);
    if (changed.has('legend')) {
      this.label = this.legend;
    }
  }

  override firstUpdated(): void {
    this._syncSegmentSelected();
    this._syncSegmentPositions();
    this._defaultValue = this.value;
  }

  override updated(changed: Map<string, unknown>): void {
    super.updated(changed);
    if (changed.has('value')) {
      this._syncSegmentSelected();
      this._syncSegmentPositions();
    }
    if (changed.has('disabled')) {
      this._syncSegmentDisabled();
    }
  }

  override formDisabledCallback(disabled: boolean): void {
    this.disabled = disabled;
    this._syncSegmentDisabled();
  }

  override render() {
    return html\`
      <fieldset
        class="civ-fieldset"
        role="radiogroup"
        aria-orientation="horizontal"
        aria-describedby="\${this._ariaDescribedBy || nothing}"
        aria-invalid="\${this.error ? 'true' : nothing}"
        aria-required="\${this.required || nothing}"
        ?disabled="\${this.disabled}"
      >
        \${renderLegend({ legend: this.legend, required: this.required, srOnly: true })}
        \${renderHint(this._hintId, this.hint, true)}
        \${renderError(this._errorId, this.error, true)}
        <div class="civ-inline-flex"></div>
      </fieldset>
    \`;
  }

  private _getSegments(): CivSegment[] {
    return Array.from(this.querySelectorAll('civ-segment')) as CivSegment[];
  }

  private _getEnabledSegments(): CivSegment[] {
    return this._getSegments().filter((s) => !s.disabled);
  }

  private _syncSegmentSelected(): void {
    this._getSegments().forEach((segment) => {
      segment.selected = segment.value === this.value;
    });
  }

  private _groupDisabledSet = new WeakSet<Element>();

  private _syncSegmentDisabled(): void {
    const segments = this._getSegments();
    if (this.disabled) {
      segments.forEach((segment) => {
        if (!segment.disabled) this._groupDisabledSet.add(segment);
        segment.disabled = true;
      });
    } else {
      segments.forEach((segment) => {
        if (this._groupDisabledSet.has(segment)) {
          segment.disabled = false;
        }
      });
      this._groupDisabledSet = new WeakSet();
    }
  }

  private _syncSegmentPositions(): void {
    const segments = this._getSegments();
    segments.forEach((segment, i) => {
      let position: string;
      if (segments.length === 1) {
        position = 'only';
      } else if (i === 0) {
        position = 'first';
      } else if (i === segments.length - 1) {
        position = 'last';
      } else {
        position = 'middle';
      }
      segment.setAttribute('data-civ-segment-position', position);
    });
  }

  private _stopChildInput(e: Event): void {
    if (e.target !== this) e.stopPropagation();
  }

  private _onChildChange(e: Event): void {
    const detail = (e as CustomEvent).detail;
    if (detail?.value == null) return;
    if (e.target === this) return;
    if (this.value === detail.value) { e.stopPropagation(); return; }

    this.value = detail.value;
    this.updateFormValue(this.value);
    this._syncSegmentSelected();

    e.stopPropagation();
    dispatch(this, 'civ-input', { value: this.value });
    dispatch(this, 'civ-change', { value: this.value });
    this.sendAnalytics('change');
  }

  private _onKeydown(e: KeyboardEvent): void {
    const segments = this._getEnabledSegments();
    if (segments.length === 0) return;

    const currentIndex = segments.findIndex((s) => s.selected);
    const nextIndex = resolveGroupNavIndex(e.key, currentIndex, segments.length, isRtl(this));

    if (nextIndex !== undefined) {
      e.preventDefault();
      const segment = segments[nextIndex];
      if (segment.value === this.value) return;
      segment.selected = true;
      this.value = segment.value;
      this.updateFormValue(this.value);
      this._syncSegmentSelected();

      const btn = segment.querySelector('button');
      if (btn) btn.focus();
      this.announce(segment.label);

      dispatch(this, 'civ-input', { value: this.value });
      dispatch(this, 'civ-change', { value: this.value });
      this.sendAnalytics('change');
    }
  }

  override formResetCallback(): void {
    this.value = this._defaultValue;
    this.error = '';
    this._syncSegmentSelected();
    this.updateFormValue(this._defaultValue || '');
    dispatch(this, 'civ-reset');
  }
}
${tagNameMap(schema)}`;
}

